import toFixed from '@/global/toFixed'

describe('toFixed 方法测试', () => {

  test('四舍五入-0或正数', () => {
    expect(toFixed(0,2)).toBe('0.00')
    expect(toFixed(0,0)).toBe('0')
    expect(toFixed(0)).toBe('0.00')
    expect(toFixed(12,2)).toBe('12.00')
    expect(toFixed(12)).toBe('12.00')
    expect(toFixed(12,1)).toBe('12.0')
    expect(toFixed(12,5)).toBe('12.00000')
    expect(toFixed(10.5,0)).toBe('11')
    expect(toFixed(10.5)).toBe('10.50')
    expect(toFixed(0.105,2)).toBe('0.11')
    expect(toFixed(0.3358,2)).toBe('0.34')
    expect(toFixed('0.3358',2)).toBe('0.34')
    expect(toFixed(1.005,2)).toBe('1.01')
    expect(toFixed(1.095,2)).toBe('1.10')
    expect(toFixed(1.005)).toBe('1.01')
    expect(toFixed(1.0005,3)).toBe('1.001')
    expect(toFixed(1.15643,3)).toBe('1.156')
    expect(toFixed(102151515.15443,3)).toBe('102151515.154')
    expect(toFixed(1,3)).toBe('1.000')
    expect(toFixed(1.05,4)).toBe('1.0500')
    expect(toFixed(1.095,0)).toBe('1')
    expect(toFixed(1.095,1)).toBe('1.1')
    expect(toFixed(1.095,2)).toBe('1.10')
    expect(toFixed(1.095,3)).toBe('1.095')
    expect(toFixed(1.095,4)).toBe('1.0950')
    expect(toFixed(1.095,5)).toBe('1.09500')
    expect(toFixed(123,0)).toBe('123')
    expect(toFixed(123.4,0)).toBe('123')
    expect(toFixed(123.5,0)).toBe('124')
    expect(toFixed(19.9,1)).toBe('19.9')
  })

  test('四舍五入-负数', () => {
    expect(toFixed(-12,2)).toBe('-12.00')
    expect(toFixed(-12)).toBe('-12.00')
    expect(toFixed(-12,1)).toBe('-12.0')
    expect(toFixed(-12,5)).toBe('-12.00000')
    expect(toFixed(-10.5,0)).toBe('-11')
    expect(toFixed(-10.5)).toBe('-10.50')
    expect(toFixed(-0.105,2)).toBe('-0.11')
    expect(toFixed(-0.3358,2)).toBe('-0.34')
    expect(toFixed('-0.3358',2)).toBe('-0.34')
    expect(toFixed(-1.005,2)).toBe('-1.01')
    expect(toFixed(-1.095,2)).toBe('-1.10')
    expect(toFixed(-1.005)).toBe('-1.01')
    expect(toFixed(-1.0005,3)).toBe('-1.001')
    expect(toFixed(-1.15643,3)).toBe('-1.156')
    expect(toFixed(-102151515.15443,3)).toBe('-102151515.154')
    expect(toFixed(-1,3)).toBe('-1.000')
    expect(toFixed(-1.05,4)).toBe('-1.0500')
    expect(toFixed(-1.095,0)).toBe('-1')
    expect(toFixed(-1.095,1)).toBe('-1.1')
    expect(toFixed(-1.095,2)).toBe('-1.10')
    expect(toFixed(-1.095,3)).toBe('-1.095')
    expect(toFixed(-1.095,4)).toBe('-1.0950')
    expect(toFixed(-1.095,5)).toBe('-1.09500')
    expect(toFixed(-123,0)).toBe('-123')
    expect(toFixed(-123.4,0)).toBe('-123')
    expect(toFixed(-123.5,0)).toBe('-124')

  })
})
